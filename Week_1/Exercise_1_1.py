def Rectangle_Area(l, w): # Calculating area of rectangles
    length = l 
    width = w
    area_rectangle1 = length * width
    print("Area of the rectangle is:", area_rectangle1)

def Circle_Area(r): # Calculating area of circle
    radius = r
    area_circle1 = 3.14 * (radius ** 2)
    print("Area of the circle is:", area_circle1)


# Execution 
Rectangle_Area(10, 5)
Rectangle_Area(15, 7)
Circle_Area(4)
Circle_Area(6)
